import { gql } from '@apollo/client';

export const ADD_USER = gql`
  mutation addUser($thoughtText: String!, $thoughtAuthor: String!) {
    addUser(thoughtText: $thoughtText, thoughtAuthor: $thoughtAuthor) {
      _id
      thoughtText
      thoughtAuthor
      createdAt
      comments {
        _id
        commentText
      }
    }
  }
`;

